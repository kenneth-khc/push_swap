/**
 * Checks if a character is alphabet or digit
 * Return 1 for true, 0 for false
**/

#include "../includes/libft.h"

int	ft_isalnum(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z')
		|| (c >= '0' && c <= '9'))
		return (1);
	return (0);
}
