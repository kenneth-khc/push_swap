/**
 * Checks if a character is alphabet
 * Return 1 for true, 0 for false
**/

#include "../includes/libft.h"

int	ft_isalpha(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	return (0);
}
