/** 
 * Locates the last occurence of c in string s 
**/

#include "../includes/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = ft_strlen(s) + 1;
	while (i--)
		if (*(s + i) == (unsigned char) c)
			return ((char *)(s + i));
	return (NULL);
}
